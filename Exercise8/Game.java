
package Exercise8;

public class Game {
    
    private String gameStart;
    
    public void starting(String gameStart){
        System.out.println(this.gameStart=gameStart);
    }
    public void pausing(String gameStart){
        System.out.println(this.gameStart=gameStart);
    }
    public void stopping(String gameStart){
        System.out.println(this.gameStart=gameStart);
    }
}
